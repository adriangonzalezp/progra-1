# Diccionario vacio.
diccionario = {}

print(len(diccionario))

# Agregamos un dato al diccionario.
diccionario[1] = "Adrian Gonzalez"

# Aqui la llave(key) es 1, y el valor asociado
# es la cadena de caracteres "Adrian Gonzalez"

print(len(diccionario))

# Vamos a imprimir todo
print(diccionario)

# Imprimir el elemento por llave
print(diccionario[1])

